/**
 * Sorts articles by newest publication date, with slug as a stable tiebreaker.
 *
 * @param entries Article entries to sort.
 * @returns New sorted array without mutating the input.
 */
export function sortNewestFirst(entries: ArticleEntry[]): ArticleEntry[] {
  return Array.from(entries).sort((a, b) => {
    const bTime = entryDate(b).getTime();
    const aTime = entryDate(a).getTime();
    const dateSort = bTime - aTime;
    return dateSort !== 0
      ? dateSort
      : articleSlug(a).localeCompare(articleSlug(b));
  });
}

/**
 * Sorts announcements by newest publication date, with slug as a stable tiebreaker.
 *
 * @param entries Announcement entries to sort.
 * @returns New sorted array without mutating the input.
 */
export function sortAnnouncementsNewestFirst(
  entries: AnnouncementEntry[],
): AnnouncementEntry[] {
  return Array.from(entries).sort((a, b) => {
    const dateSort = b.data.date.getTime() - a.data.date.getTime();
    return dateSort !== 0
      ? dateSort
      : announcementSlug(a).localeCompare(announcementSlug(b));
  });
}

function imageSource(value: unknown): string | undefined {
  if (
    typeof value === "object" &&
    value !== null &&
    "src" in value &&
    typeof value.src === "string"
  ) {
    return value.src;
  }

  return undefined;
}

function siteRoute(key: SiteRouteKey): string {
  switch (key) {
    case "allArticles": {
      return siteConfig.routes.allArticles;
    }
    case "announcements": {
      return siteConfig.routes.announcements;
    }
    case "articles": {
      return siteConfig.routes.articles;
    }
    case "authors": {
      return siteConfig.routes.authors;
    }
    case "bibliography": {
      return siteConfig.routes.bibliography;
    }
    case "categories": {
      return siteConfig.routes.categories;
    }
    case "collections": {
      return siteConfig.routes.collections;
    }
    case "feed": {
      return siteConfig.routes.feed;
    }
    case "home": {
      return siteConfig.routes.home;
    }
    case "search": {
      return siteConfig.routes.search;
    }
    case "tags": {
      return siteConfig.routes.tags;
    }
  }
}

function withTrailingSlash(path: string): string {
  if (path === "") {
    return "/";
  }
  return path.endsWith("/") ? path : `${path}/`;
}
