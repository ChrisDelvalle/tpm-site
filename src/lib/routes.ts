/**
 * Converts free-form text into the URL-safe slug format used by the site.
 *
 * @param value Raw text to normalize.
 * @returns Lowercase dash-separated slug text.
 */
export function normalizeSlug(value: string): string {
  return value
    .trim()
    .toLowerCase()
    .replace(/&/g, "and")
    .replace(/[^a-z0-9]+/g, "-")
    .replace(/^-|-$/g, "");
}

