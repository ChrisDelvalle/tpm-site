/**
 * Reads the stable URL slug for an announcement.
 *
 * @param entry Announcement content entry.
 * @returns Announcement slug generated by Astro's content collection ID.
 */
function announcementSlug(entry: AnnouncementEntry): string {
  return entry.id;
}

/**
 * Builds the canonical site URL path for an announcement slug.
 *
 * @param slug Announcement slug generated from the content entry ID.
 * @returns Absolute-path URL with the configured trailing slash.
 */
export function announcementUrl(slug: string): string {
  return withTrailingSlash(`${announcementsIndexUrl()}${slug}`);
}

/**
 * Fails fast when multiple announcement entries resolve to the same public slug.
 *
 * @param entries Announcement entries to validate.
 */
export function assertUniqueAnnouncementSlugs(
  entries: AnnouncementEntry[],
): void {
  const seen = new Map<string, string>();

  for (const entry of entries) {
    const slug = announcementSlug(entry);
    const previous = seen.get(slug);

    if (previous !== undefined) {
      throw new Error(
        `Duplicate announcement slug "${slug}" for "${previous}" and "${entry.id}".`,
      );
    }

    seen.set(slug, entry.id);
  }
}

