/**
 * Fails fast when multiple article entries resolve to the same public slug.
 *
 * @param entries Article entries to validate.
 */
export function assertUniqueArticleSlugs(entries: ArticleEntry[]): void {
  const seen = new Map<string, string>();

  for (const entry of entries) {
    const slug = articleSlug(entry);
    const previous = seen.get(slug);

    if (previous !== undefined) {
      throw new Error(
        `Duplicate article slug "${slug}" for "${previous}" and "${entry.id}".`,
      );
    }

    if (RESERVED_ARTICLE_SLUGS.has(slug)) {
      throw new Error(`Article slug "${slug}" is reserved for a site route.`);
    }

    seen.set(slug, entry.id);
  }
}

