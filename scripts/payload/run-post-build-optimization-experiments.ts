/** Command used by a generated-output optimization gate. */
export interface PostBuildOptimizationGateCommand {
  args: string[];
  binary: "bunx";
  label: string;
}

/** Result from one generated-output optimization gate. */
export interface PostBuildOptimizationGateResult {
  command: PostBuildOptimizationGateCommand;
  exitCode: number;
  output: string;
}

/** Policy for how an optimization scenario may affect production adoption. */
export type PostBuildOptimizationScenarioPolicy =
  | "candidate"
  | "measurement-only";

/** Supported standalone post-build transform. */
export type PostBuildTransformName = BuildOutputTransformName;

/** One reproducible post-build optimization scenario. */
export interface PostBuildOptimizationScenario {
  name: string;
  policy: PostBuildOptimizationScenarioPolicy;
  rationale: string;
  transforms: PostBuildTransformName[];
}

/** Paths allocated to one post-build optimization scenario. */
export interface PostBuildOptimizationScenarioPaths {
  outputDir: string;
  scenarioRoot: string;
}

/** Dependency-injected gate runner for tests. */
export type PostBuildOptimizationGateRunner = (
  command: PostBuildOptimizationGateCommand,
  cwd: string,
) => Promise<PostBuildOptimizationGateResult>;

/** Dependency-injected build verifier for tests. */
export type PostBuildOptimizationVerifyRunner = (
  outputDir: string,
  cwd: string,
) => Promise<PostBuildOptimizationGateResult>;

/** Result from one post-build optimization scenario. */
export interface PostBuildOptimizationScenarioResult {
  gates: PostBuildOptimizationGateResult[];
  output: PayloadReport;
  productionEligible: boolean;
  scenario: PostBuildOptimizationScenario;
}

/** Options for the full post-build optimization experiment suite. */
export interface PostBuildOptimizationSuiteOptions {
  cwd: string;
  gateRunner?: PostBuildOptimizationGateRunner;
  outputRoot: string;
  reportFile?: string;
  scenarios?: PostBuildOptimizationScenario[];
  sourceDir: string;
  verifyRunner?: PostBuildOptimizationVerifyRunner;
}

/** Complete post-build optimization suite result. */
export interface PostBuildOptimizationSuiteResult {
  baseline: PayloadReport;
  recommendedScenario: PostBuildOptimizationScenarioResult | undefined;
  report: string;
  results: PostBuildOptimizationScenarioResult[];
}

