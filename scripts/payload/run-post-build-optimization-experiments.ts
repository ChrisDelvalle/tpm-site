/**
 * Formats the standalone post-build optimization experiment report.
 *
 * @param result Suite result without its final report string.
 * @returns Markdown report.
 */
export function formatPostBuildOptimizationSuiteReport(
  result: Omit<PostBuildOptimizationSuiteResult, "report">,
): string {
  const lines = [
    "# Standalone Post-Build Optimization Experiments",
    "",
    "This report is generated by `bun run payload:postbuild:experiments` so",
    "standalone optimizers can be evaluated independently of Astro/Vite config",
    "support. The package script rebuilds raw output first; each scenario then",
    "operates on a copied `dist/` directory and runs generated-output gates plus",
    "raw/gzip/Brotli payload measurement.",
    "",
    "## Tooling Context",
    "",
    "- `oxc-minify` can be used as a standalone Node API over generated JS;",
    "  upstream labels the minifier as alpha and recommends thorough production",
    "  testing.",
    "- `lightningcss` is available as a standalone parser/minifier and is already",
    "  part of the Vite/Astro ecosystem.",
    "- `svgo` is a standalone SVG optimizer and Node API; this suite keeps",
    "  `viewBox` preservation explicit.",
    "",
    "## Baseline",
    "",
    payloadGroupTable([
      ["All assets", result.baseline.allFiles],
      ["Gzip/Brotli-eligible assets", result.baseline.gzipEligibleFiles],
      ["HTML assets", result.baseline.htmlFiles],
      ["CSS assets", groupByExtension(result.baseline, ".css")],
      ["JS assets", groupByExtension(result.baseline, ".js")],
      ["SVG assets", groupByExtension(result.baseline, ".svg")],
    ]),
    "",
    "## Scenario Results",
    "",
    scenarioTable(result.results, result.baseline),
    "",
    "## Scenario Details",
    "",
    ...result.results.flatMap((scenarioResult) =>
      formatScenarioDetails(scenarioResult, result.baseline),
    ),
    "## Conclusion",
    "",
    conclusionText(result.recommendedScenario),
    "",
    "## Adoption Notes",
    "",
    "- A passing payload experiment is not enough for production adoption when",
    "  JavaScript output changes. The full browser, accessibility, HTML, build",
    "  verification, and release gates must pass with the optimized output.",
    "- Static generated-output verification recognizes Astro's prefetch runtime",
    "  by checking the stable runtime behaviors instead of exact generated-code",
    "  quote style or whitespace.",
    "- Measurement-only Oxc aggressive output is intentionally excluded from",
    "  production recommendations until its assumptions are proven against this",
    "  site's actual browser behavior.",
  ];

  return lines.join("\n");
}

/**
 * Runs every configured standalone post-build optimization scenario.
 *
 * @param options Suite options.
 * @param options.cwd Repository root.
 * @param options.gateRunner Gate command runner.
 * @param options.outputRoot Directory where copied scenario outputs are written.
 * @param options.reportFile Optional Markdown report path.
 * @param options.scenarios Scenarios to run.
 * @param options.sourceDir Source build output directory.
 * @param options.verifyRunner Build verification gate runner.
 * @returns Suite result and formatted Markdown report.
 */
export async function runPostBuildOptimizationSuite({
  cwd,
  gateRunner = runGateCommand,
  outputRoot,
  reportFile,
  scenarios = Array.from(defaultScenarios),
  sourceDir,
  verifyRunner = runVerifyBuildGate,
}: PostBuildOptimizationSuiteOptions): Promise<PostBuildOptimizationSuiteResult> {
  const resolvedSourceDir = path.resolve(cwd, sourceDir);
  const resolvedOutputRoot = path.resolve(cwd, outputRoot);
  const baseline = collectPayloadReport({ distDir: resolvedSourceDir });
  const results: PostBuildOptimizationScenarioResult[] = [];

  for (const scenario of scenarios) {
    const paths = {
      outputDir: path.join(resolvedOutputRoot, scenario.name),
      scenarioRoot: path.join(resolvedOutputRoot, scenario.name),
    };
    copyBuildOutput(resolvedSourceDir, paths.outputDir);
    applyScenarioTransforms(paths.outputDir, scenario);

    const output = collectPayloadReport({ distDir: paths.outputDir });
    const gates = await runScenarioGates({
      cwd,
      gateRunner,
      outputDir: paths.outputDir,
      verifyRunner,
    });

    results.push({
      gates,
      output,
      productionEligible:
        scenario.policy === "candidate" &&
        gates.every((gate) => gate.exitCode === 0),
      scenario,
    });
  }

  const recommendedScenario = chooseRecommendedScenario(results, baseline);
  const report = formatPostBuildOptimizationSuiteReport({
    baseline,
    recommendedScenario,
    results,
  });

  if (reportFile !== undefined) {
    const resolvedReportFile = path.resolve(cwd, reportFile);
    mkdirSync(path.dirname(resolvedReportFile), { recursive: true });
    writeFileSync(resolvedReportFile, `${report}\n`);
  }

  return {
    baseline,
    recommendedScenario,
    report,
    results,
  };
}

/**
 * Runs the standalone post-build optimization experiment CLI.
 *
 * @param args Command-line arguments without executable prefix.
 * @param cwd Repository root.
 * @returns Process exit code.
 */
export async function runPostBuildOptimizationSuiteCli(
  args = process.argv.slice(2),
  cwd = process.cwd(),
): Promise<number> {
  if (args.includes("--help") || args.includes("-h")) {
    console.log(usage());
    return 0;
  }

  try {
    const sourceDir = readValueArg(args, "--source") ?? "dist";
    const outputRoot =
      readValueArg(args, "--out") ??
      path.join("tmp", "post-build-optimization");
    const reportFile = args.includes("--no-report")
      ? undefined
      : (readValueArg(args, "--report") ?? defaultReportFile);
    const scenarioNames = readRepeatedValueArg(args, "--scenario");
    const scenarios =
      scenarioNames.length === 0
        ? Array.from(defaultScenarios)
        : scenariosByName(scenarioNames);
    const suiteOptions: PostBuildOptimizationSuiteOptions = {
      cwd,
      outputRoot,
      scenarios,
      sourceDir,
    };
    if (reportFile !== undefined) {
      suiteOptions.reportFile = reportFile;
    }

    const result = await runPostBuildOptimizationSuite(suiteOptions);

    if (args.includes("--json")) {
      console.log(JSON.stringify(result, null, 2));
    } else {
      console.log(result.report);
    }

    return 0;
  } catch (error) {
    console.error(error instanceof Error ? error.message : String(error));
    return 1;
  }
}

function applyScenarioTransforms(
  outputDir: string,
  scenario: PostBuildOptimizationScenario,
): void {
  optimizeBuildOutput({
    outputDir,
    transforms: scenario.transforms,
  });
}

function buildVerificationHasIssues(issues: BuildVerificationIssues): boolean {
  const issueLists: string[][] = [
    issues.articleCountIssues,
    issues.brokenLinks,
    issues.catalogLeaks,
    issues.draftLeaks,
    issues.invalidLegacyRedirects,
    issues.missingArticleJsonLd,
    issues.missingLegacyRedirects,
    issues.missingRequired,
    issues.sourceMaps,
    issues.unexpectedClientScripts,
    issues.unexpectedDatedPages,
    issues.unexpectedHydrationBoundaries,
  ];

  return issueLists.some((issueList) => issueList.length > 0);
}

function chooseRecommendedScenario(
  results: PostBuildOptimizationScenarioResult[],
  baseline: PayloadReport,
): PostBuildOptimizationScenarioResult | undefined {
  const eligible = results.filter(
    (result) =>
      result.productionEligible &&
      totalBrotliDelta(result.output, baseline) < 0,
  );

  return eligible.sort(
    (left, right) =>
      totalBrotliDelta(left.output, baseline) -
      totalBrotliDelta(right.output, baseline),
  )[0];
}

function commandEnvironment(): NodeJS.ProcessEnv {
  const env = { ...process.env };
  env["NO_COLOR"] = "1";
  delete env["FORCE_COLOR"];
  return env;
}

function commandLine(command: PostBuildOptimizationGateCommand): string {
  return [command.binary, ...command.args.map(displayCommandArg)].join(" ");
}

function conclusionText(
  recommendedScenario: PostBuildOptimizationScenarioResult | undefined,
): string {
  if (recommendedScenario === undefined) {
    return [
      "No standalone post-build optimizer currently qualifies for production",
      "adoption from these gates alone. Keep the experiment harness and revisit",
      "candidate tools when the full browser/release gate can be run against",
      "optimized output.",
    ].join("\n");
  }

  return [
    `Best passing candidate by Brotli size: \`${recommendedScenario.scenario.name}\`.`,
    "This is a candidate for a production post-build step, not an automatic",
    "adoption. It still needs the full release gate against optimized output.",
  ].join("\n");
}

function configuredStringRedirects(): Record<string, string> {
  return Object.fromEntries(
    Object.entries(astroConfig.redirects ?? {}).flatMap(([source, target]) =>
      typeof target === "string" ? [[source, target]] : [],
    ),
  );
}

function conciseGateOutput(output: string): string[] {
  return output
    .trim()
    .split(/\r?\n/)
    .map((line) => line.replaceAll(`${process.cwd()}${path.sep}`, ""))
    .filter((line) => line.trim() !== "")
    .slice(0, 24);
}

function copyBuildOutput(sourceDir: string, outputDir: string): void {
  mkdirSync(path.dirname(outputDir), { recursive: true });
  rmSync(outputDir, { force: true, recursive: true });
  cpSync(sourceDir, outputDir, { recursive: true });
}

function displayCommandArg(arg: string): string {
  const relativePath = path.relative(process.cwd(), arg);

  if (
    relativePath !== "" &&
    !relativePath.startsWith("..") &&
    !path.isAbsolute(relativePath)
  ) {
    return relativePath.split(path.sep).join("/");
  }

  return arg;
}

function formatBytes(bytes: number | undefined): string {
  if (bytes === undefined) {
    return "n/a";
  }

  return `${new Intl.NumberFormat("en-US").format(bytes)} B`;
}

function formatDelta(bytes: number): string {
  const sign = bytes > 0 ? "+" : "";
  return `${sign}${new Intl.NumberFormat("en-US").format(bytes)} B`;
}

function formatGate(gate: PostBuildOptimizationGateResult): string {
  return gate.exitCode === 0 ? "pass" : `fail (${gate.exitCode})`;
}

function formatScenarioDetails(
  result: PostBuildOptimizationScenarioResult,
  baseline: PayloadReport,
): string[] {
  const failedOutput = result.gates
    .filter((gate) => gate.exitCode !== 0)
    .flatMap((gate) => conciseGateOutput(gate.output));
  const lines = [
    `### ${result.scenario.name}`,
    "",
    `Policy: ${result.scenario.policy}`,
    "",
    result.scenario.rationale,
    "",
    payloadGroupTable([
      ["All assets", result.output.allFiles],
      ["Gzip/Brotli-eligible assets", result.output.gzipEligibleFiles],
      ["HTML assets", result.output.htmlFiles],
      ["CSS assets", groupByExtension(result.output, ".css")],
      ["JS assets", groupByExtension(result.output, ".js")],
      ["SVG assets", groupByExtension(result.output, ".svg")],
    ]),
    "",
    `Compressed delta vs baseline: ${formatDelta(totalBrotliDelta(result.output, baseline))} Brotli, ${formatDelta(totalGzipDelta(result.output, baseline))} gzip.`,
    "",
    "Gates:",
    ...result.gates.map(
      (gate) =>
        `- ${gate.command.label}: ${formatGate(gate)} via \`${commandLine(gate.command)}\``,
    ),
  ];

  if (failedOutput.length > 0) {
    lines.push("", "Failure excerpt:", "", "```text", ...failedOutput, "```");
  }

  return [...lines, ""];
}

function groupByExtension(
  report: PayloadReport,
  extension: string,
): PayloadGroup {
  return (
    report.byExtension.find((group) => group.extension === extension) ?? {
      brotliBytes: undefined,
      extension,
      files: 0,
      gzipBytes: undefined,
      rawBytes: 0,
    }
  );
}

function htmlValidateCommand(
  outputDir: string,
): PostBuildOptimizationGateCommand {
  return {
    args: [
      "html-validate",
      "--max-warnings=0",
      path.join(outputDir, "index.html"),
      path.join(outputDir, "404.html"),
      path.join(outputDir, "about", "**", "*.html"),
      path.join(outputDir, "articles", "index.html"),
      path.join(outputDir, "authors", "**", "*.html"),
      path.join(outputDir, "categories", "**", "*.html"),
      path.join(outputDir, "search", "**", "*.html"),
    ],
    binary: "bunx",
    label: "HTML validation",
  };
}

function payloadGroupTable(
  groups: Array<
    [
      string,
      Pick<PayloadGroup, "brotliBytes" | "files" | "gzipBytes" | "rawBytes">,
    ]
  >,
): string {
  return [
    "| Group | Files | Raw Bytes | Gzip Bytes | Brotli Bytes |",
    "| --- | ---: | ---: | ---: | ---: |",
    ...groups.map(
      ([label, group]) =>
        `| ${label} | ${group.files} | ${formatBytes(group.rawBytes)} | ${formatBytes(group.gzipBytes)} | ${formatBytes(group.brotliBytes)} |`,
    ),
  ].join("\n");
}

function readRepeatedValueArg(args: string[], flag: string): string[] {
  return args.flatMap((arg, index) => {
    if (arg !== flag) {
      return [];
    }

    const value = args[index + 1];
    if (value === undefined || value.startsWith("-")) {
      throw new Error(`Missing value for ${flag}.`);
    }

    return [value];
  });
}

function readValueArg(args: string[], flag: string): string | undefined {
  const index = args.indexOf(flag);

  if (index === -1) {
    return undefined;
  }

  const value = args[index + 1];
  if (value === undefined || value.startsWith("-")) {
    throw new Error(`Missing value for ${flag}.`);
  }

  return value;
}

async function runGateCommand(
  command: PostBuildOptimizationGateCommand,
  cwd: string,
): Promise<PostBuildOptimizationGateResult> {
  return new Promise<PostBuildOptimizationGateResult>((resolve) => {
    const child = spawn(command.binary, command.args, {
      cwd,
      env: commandEnvironment(),
      stdio: ["ignore", "pipe", "pipe"],
    });
    const chunks: Buffer[] = [];

    child.stdout.on("data", (chunk: Buffer) => {
      chunks.push(chunk);
    });
    child.stderr.on("data", (chunk: Buffer) => {
      chunks.push(chunk);
    });
    child.on("error", (error) => {
      resolve({
        command,
        exitCode: 1,
        output: error.message,
      });
    });
    child.on("close", (code) => {
      resolve({
        command,
        exitCode: code ?? 1,
        output: Buffer.concat(chunks).toString("utf8"),
      });
    });
  });
}

async function runScenarioGates({
  cwd,
  gateRunner,
  outputDir,
  verifyRunner,
}: {
  cwd: string;
  gateRunner: PostBuildOptimizationGateRunner;
  outputDir: string;
  verifyRunner: PostBuildOptimizationVerifyRunner;
}): Promise<PostBuildOptimizationGateResult[]> {
  return [
    await gateRunner(htmlValidateCommand(outputDir), cwd),
    await verifyRunner(outputDir, cwd),
  ];
}

async function runVerifyBuildGate(
  outputDir: string,
  cwd: string,
): Promise<PostBuildOptimizationGateResult> {
  const command = {
    args: ["scripts/verify-build.ts", "--quiet"],
    binary: "bunx",
    label: "Build verification",
  } satisfies PostBuildOptimizationGateCommand;

  try {
    const result = await verifyBuild({
      articleDir: path.join(cwd, "src/content/articles"),
      categoryDir: path.join(cwd, "src/content/categories"),
      distDir: outputDir,
      expectedRedirects: configuredStringRedirects(),
    });
    const output = formatBuildVerificationReport(result);

    return {
      command,
      exitCode: buildVerificationHasIssues(result.issues) ? 1 : 0,
      output,
    };
  } catch (error) {
    return {
      command,
      exitCode: 1,
      output: error instanceof Error ? error.message : String(error),
    };
  }
}

function scenarioTable(
  results: PostBuildOptimizationScenarioResult[],
  baseline: PayloadReport,
): string {
  return [
    "| Scenario | Policy | Brotli Delta | Gzip Delta | CSS Brotli | JS Brotli | SVG Brotli | Gates | Production Eligible |",
    "| --- | --- | ---: | ---: | ---: | ---: | ---: | --- | --- |",
    ...results.map((result) => {
      const css = groupByExtension(result.output, ".css").brotliBytes;
      const js = groupByExtension(result.output, ".js").brotliBytes;
      const svg = groupByExtension(result.output, ".svg").brotliBytes;

      return `| \`${result.scenario.name}\` | ${result.scenario.policy} | ${formatDelta(totalBrotliDelta(result.output, baseline))} | ${formatDelta(totalGzipDelta(result.output, baseline))} | ${formatBytes(css)} | ${formatBytes(js)} | ${formatBytes(svg)} | ${result.gates.map(formatGate).join(", ")} | ${result.productionEligible ? "yes" : "no"} |`;
    }),
  ].join("\n");
}

function scenariosByName(names: string[]): PostBuildOptimizationScenario[] {
  return names.map((name) => {
    const scenario = defaultScenarios.find((item) => item.name === name);

    if (scenario === undefined) {
      throw new Error(
        `Unknown post-build optimization scenario "${name}". Expected one of: ${defaultScenarios.map((item) => item.name).join(", ")}.`,
      );
    }

    return scenario;
  });
}

function totalBrotliDelta(
  output: PayloadReport,
  baseline: PayloadReport,
): number {
  return (
    (output.gzipEligibleFiles.brotliBytes ?? 0) -
    (baseline.gzipEligibleFiles.brotliBytes ?? 0)
  );
}

function totalGzipDelta(
  output: PayloadReport,
  baseline: PayloadReport,
): number {
  return (
    (output.gzipEligibleFiles.gzipBytes ?? 0) -
    (baseline.gzipEligibleFiles.gzipBytes ?? 0)
  );
}

function usage(): string {
  return `Usage: bun run payload:postbuild:experiments [--source <dir>] [--out <dir>] [--report <file>] [--scenario <name>] [--no-report] [--json]

Run standalone post-build optimization scenarios against copied build output,
validate generated HTML, verify build output, and compare raw/gzip/Brotli
payloads. Use the package script to rebuild the raw baseline first, or pass a
custom --source directory. Use --scenario multiple times to limit the suite.

Default report path: ${defaultReportFile}`;
}

if (import.meta.main) {
  try {
    process.exitCode = await runPostBuildOptimizationSuiteCli();
  } catch (error) {
    console.error(error instanceof Error ? error.message : String(error));
    process.exitCode = 1;
  }
}
