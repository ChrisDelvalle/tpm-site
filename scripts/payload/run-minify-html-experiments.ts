import { spawn } from "node:child_process";
import { mkdirSync, writeFileSync } from "node:fs";
import path from "node:path";

import {
  type MinifyHtmlExperimentName,
  minifyHtmlExperimentNames,
  type MinifyHtmlExperimentResult,
  runMinifyHtmlExperiment,
} from "./minify-html-experiment";

/** Command used by an experiment gate. */
export interface MinifyHtmlGateCommand {
  args: string[];
  label: string;
}

/** Result from one experiment gate command. */
export interface MinifyHtmlGateResult {
  command: MinifyHtmlGateCommand;
  exitCode: number;
  output: string;
}

/** Dependency-injected command runner for experiment gates. */
export type MinifyHtmlGateRunner = (
  command: MinifyHtmlGateCommand,
  cwd: string,
) => Promise<MinifyHtmlGateResult>;

/** Policy for how a scenario may affect production recommendations. */
export type MinifyHtmlScenarioPolicy = "candidate" | "measurement-only";

/** One reproducible minify-html scenario. */
export interface MinifyHtmlScenario {
  name: MinifyHtmlExperimentName;
  policy: MinifyHtmlScenarioPolicy;
  rationale: string;
}

/** Result from one minify-html scenario plus its gates. */
export interface MinifyHtmlScenarioResult {
  experiment: MinifyHtmlExperimentResult;
  gates: MinifyHtmlGateResult[];
  productionEligible: boolean;
  scenario: MinifyHtmlScenario;
}

/** Options for running the full reproducible experiment suite. */
export interface MinifyHtmlExperimentSuiteOptions {
  cwd: string;
  failIfNoProductionCandidate?: boolean;
  gateRunner?: MinifyHtmlGateRunner;
  outputRoot: string;
  reportFile?: string;
  scenarios?: MinifyHtmlScenario[];
  sourceDir: string;
}

/** Complete result for a reproducible minify-html experiment suite run. */
export interface MinifyHtmlExperimentSuiteResult {
  recommendedScenario: MinifyHtmlScenarioResult | undefined;
  report: string;
  results: MinifyHtmlScenarioResult[];
}

const defaultReportFile = path.join(
  "tmp",
  "minify-html",
  "experiment-report.md",
);

const defaultScenarios = [
  {
    name: "conservative",
    policy: "candidate",
    rationale:
      "Conservative standards-first candidate: keep optional tags, minify inline CSS, and leave inline JavaScript alone.",
  },
  {
    name: "inline-js",
    policy: "candidate",
    rationale:
      "Candidate only if inline JavaScript minification proves smaller after compression and preserves theme, search, and interaction behavior.",
  },
  {
    name: "optional-tags",
    policy: "candidate",
    rationale:
      "Candidate only if optional tag omission passes strict validation and provides meaningful compressed savings.",
  },
  {
    name: "noncompliant-measurement",
    policy: "measurement-only",
    rationale:
      "Upper-bound measurement for noncompliant options; never production-eligible without explicit standards tradeoff approval.",
  },
] as const satisfies readonly MinifyHtmlScenario[];

/**
 * Formats a full reproducible minify-html experiment report.
 *
 * @param result Suite result without its final formatted report.
 * @returns Markdown experiment report.
 */
export function formatMinifyHtmlExperimentSuiteReport(
  result: Omit<MinifyHtmlExperimentSuiteResult, "report">,
): string {
  const baseline = result.results[0]?.experiment.before;
  const lines = [
    "# minify-html Experiments",
    "",
    "This report is generated by `bun run payload:minify-html:experiments` so",
    "the same scenarios can be rerun after site, dependency, or requirements",
    "changes. The package script rebuilds raw output first. It measures raw",
    "bytes, gzip bytes, and Brotli bytes because the production target is static HTML",
    "served by compression-capable hosting.",
    "",
    "## Baseline",
    "",
    baseline === undefined
      ? "No baseline was captured."
      : payloadGroupTable([
          ["All assets", baseline.allFiles],
          ["Gzip/Brotli-eligible assets", baseline.gzipEligibleFiles],
          ["HTML assets", baseline.htmlFiles],
        ]),
    "",
    "## Scenario Results",
    "",
    scenarioTable(result.results),
    "",
    "## Scenario Details",
    "",
    ...result.results.flatMap(formatScenarioDetails),
    "## Conclusion",
    "",
    conclusionText(result.recommendedScenario),
    "",
    "## One-Pass Feasibility",
    "",
    "The one-pass variant remains documented in `docs/performance/minify-html-research.md`.",
    "This reproducible suite exercises the standard `@minify-html/node` binding",
    "because it is the Bun-friendly cross-platform path currently available to",
    "the project. One-pass should be added to this suite only if a maintained",
    "Node/Bun-friendly package or CLI path is proven.",
  ];

  return lines.join("\n");
}

/**
 * Runs every configured minify-html scenario against copied build output.
 *
 * @param options Suite options.
 * @param options.cwd Working directory for relative paths and gate commands.
 * @param options.failIfNoProductionCandidate Whether to fail when no candidate passes.
 * @param options.gateRunner Command runner used by scenario gates.
 * @param options.outputRoot Directory where copied scenario outputs are written.
 * @param options.reportFile Markdown report path to write.
 * @param options.scenarios Scenarios to execute.
 * @param options.sourceDir Built output directory to copy and minify.
 * @returns Reproducible suite result and formatted Markdown report.
 */
export async function runMinifyHtmlExperimentSuite({
  cwd,
  failIfNoProductionCandidate = false,
  gateRunner = runGateCommand,
  outputRoot,
  reportFile,
  scenarios = Array.from(defaultScenarios),
  sourceDir,
}: MinifyHtmlExperimentSuiteOptions): Promise<MinifyHtmlExperimentSuiteResult> {
  const resolvedSource = path.resolve(cwd, sourceDir);
  const resolvedOutputRoot = path.resolve(cwd, outputRoot);
  const results: MinifyHtmlScenarioResult[] = [];

  for (const scenario of scenarios) {
    const scenarioOutputDir = path.join(resolvedOutputRoot, scenario.name);
    const experiment = runMinifyHtmlExperiment({
      configName: scenario.name,
      outputDir: scenarioOutputDir,
      sourceDir: resolvedSource,
    });
    const gates = await runScenarioGates({
      cwd,
      gateRunner,
      outputDir: scenarioOutputDir,
    });

    results.push({
      experiment,
      gates,
      productionEligible:
        scenario.policy === "candidate" &&
        gates.every((gate) => gate.exitCode === 0),
      scenario,
    });
  }

  const recommendedScenario = chooseRecommendedScenario(results);
  if (failIfNoProductionCandidate && recommendedScenario === undefined) {
    throw new Error("No minify-html scenario passed every production gate.");
  }

  const report = formatMinifyHtmlExperimentSuiteReport({
    recommendedScenario,
    results,
  });

  if (reportFile !== undefined) {
    const resolvedReportFile = path.resolve(cwd, reportFile);
    mkdirSync(path.dirname(resolvedReportFile), { recursive: true });
    writeFileSync(resolvedReportFile, `${report}\n`);
  }

  return {
    recommendedScenario,
    report,
    results,
  };
}

/**
 * Runs the reproducible minify-html experiment-suite CLI.
 *
 * @param args Command-line arguments without executable prefix.
 * @param cwd Working directory for relative paths and subprocesses.
 * @returns Process exit code.
 */
export async function runMinifyHtmlExperimentSuiteCli(
  args = process.argv.slice(2),
  cwd = process.cwd(),
): Promise<number> {
  if (args.includes("--help") || args.includes("-h")) {
    console.log(usage());
    return 0;
  }

  try {
    const sourceDir = readValueArg(args, "--source") ?? "dist";
    const outputRoot =
      readValueArg(args, "--out") ?? path.join("tmp", "minify-html");
    const reportFile = args.includes("--no-report")
      ? undefined
      : (readValueArg(args, "--report") ?? defaultReportFile);
    const scenarioNames = readRepeatedValueArg(args, "--scenario");
    const scenarios =
      scenarioNames.length === 0
        ? Array.from(defaultScenarios)
        : scenariosByName(scenarioNames);
    const suiteOptions: MinifyHtmlExperimentSuiteOptions = {
      cwd,
      failIfNoProductionCandidate: args.includes(
        "--require-production-candidate",
      ),
      outputRoot,
      scenarios,
      sourceDir,
    };
    if (reportFile !== undefined) {
      suiteOptions.reportFile = reportFile;
    }

    const result = await runMinifyHtmlExperimentSuite(suiteOptions);

    if (args.includes("--json")) {
      console.log(JSON.stringify(result, null, 2));
    } else {
      console.log(result.report);
    }

    return 0;
  } catch (error) {
    console.error(error instanceof Error ? error.message : String(error));
    return 1;
  }
}

function chooseRecommendedScenario(
  results: MinifyHtmlScenarioResult[],
): MinifyHtmlScenarioResult | undefined {
  const eligible = results.filter((result) => result.productionEligible);

  return eligible.sort(
    (left, right) => htmlBrotliDelta(left) - htmlBrotliDelta(right),
  )[0];
}

function commandEnvironment(): NodeJS.ProcessEnv {
  // Coverage note: this environment adapter is only used by the real
  // subprocess runner; suite tests inject their gate runner.
  const env = { ...process.env };
  env["NO_COLOR"] = "1";
  delete env["FORCE_COLOR"];
  return env;
}

function commandLine(command: MinifyHtmlGateCommand): string {
  return ["bunx", ...command.args.map(displayCommandArg)].join(" ");
}

function conclusionText(
  recommendedScenario: MinifyHtmlScenarioResult | undefined,
): string {
  if (recommendedScenario === undefined) {
    return [
      "No tested scenario is production-eligible. Do not add `minify-html` to",
      "the production build until a scenario passes strict validation and the",
      "rest of the release gates.",
    ].join("\n");
  }

  return [
    `Recommended candidate: \`${recommendedScenario.scenario.name}\`.`,
    "It passed the experiment gates and produced the smallest Brotli-compressed",
    "HTML among production-eligible scenarios.",
  ].join("\n");
}

function formatBytes(bytes: number | undefined): string {
  if (bytes === undefined) {
    return "n/a";
  }

  return `${new Intl.NumberFormat("en-US").format(bytes)} B`;
}

function formatDelta(bytes: number): string {
  const sign = bytes > 0 ? "+" : "";
  return `${sign}${new Intl.NumberFormat("en-US").format(bytes)} B`;
}

function formatGate(gate: MinifyHtmlGateResult): string {
  return gate.exitCode === 0 ? "pass" : `fail (${gate.exitCode})`;
}

function formatScenarioDetails(result: MinifyHtmlScenarioResult): string[] {
  const lines = [
    `### ${result.scenario.name}`,
    "",
    `Policy: ${result.scenario.policy}`,
    "",
    result.scenario.rationale,
    "",
    "Gates:",
    ...result.gates.map(
      (gate) =>
        `- ${gate.command.label}: ${formatGate(gate)} via \`${commandLine(gate.command)}\``,
    ),
  ];
  const failedOutput = result.gates
    .filter((gate) => gate.exitCode !== 0)
    .flatMap((gate) => conciseGateOutput(gate.output));

  if (failedOutput.length > 0) {
    lines.push("", "Failure excerpt:", "", "```text", ...failedOutput, "```");
  }

  return [...lines, ""];
}

function htmlBrotliDelta(result: MinifyHtmlScenarioResult): number {
  return (
    (result.experiment.after.htmlFiles.brotliBytes ?? 0) -
    (result.experiment.before.htmlFiles.brotliBytes ?? 0)
  );
}

function htmlGzipDelta(result: MinifyHtmlScenarioResult): number {
  return (
    (result.experiment.after.htmlFiles.gzipBytes ?? 0) -
    (result.experiment.before.htmlFiles.gzipBytes ?? 0)
  );
}

function htmlRawDelta(result: MinifyHtmlScenarioResult): number {
  return (
    result.experiment.after.htmlFiles.rawBytes -
    result.experiment.before.htmlFiles.rawBytes
  );
}

function htmlValidateCommand(outputDir: string): MinifyHtmlGateCommand {
  return {
    args: [
      "html-validate",
      "--max-warnings=0",
      path.join(outputDir, "index.html"),
      path.join(outputDir, "404.html"),
      path.join(outputDir, "about", "**", "*.html"),
      path.join(outputDir, "articles", "index.html"),
      path.join(outputDir, "authors", "**", "*.html"),
      path.join(outputDir, "categories", "**", "*.html"),
      path.join(outputDir, "search", "**", "*.html"),
    ],
    label: "HTML validation",
  };
}

function payloadGroupTable(
  groups: Array<
    [
      string,
      {
        brotliBytes: number | undefined;
        files: number;
        gzipBytes: number | undefined;
        rawBytes: number;
      },
    ]
  >,
): string {
  return [
    "| Group | Files | Raw Bytes | Gzip Bytes | Brotli Bytes |",
    "| --- | ---: | ---: | ---: | ---: |",
    ...groups.map(
      ([label, group]) =>
        `| ${label} | ${group.files} | ${formatBytes(group.rawBytes)} | ${formatBytes(group.gzipBytes)} | ${formatBytes(group.brotliBytes)} |`,
    ),
  ].join("\n");
}

function readRepeatedValueArg(args: string[], flag: string): string[] {
  return args.flatMap((arg, index) => {
    if (arg !== flag) {
      return [];
    }

    const value = args[index + 1];
    if (value === undefined || value.startsWith("-")) {
      throw new Error(`Missing value for ${flag}.`);
    }

    return [value];
  });
}

function readValueArg(args: string[], flag: string): string | undefined {
  const index = args.indexOf(flag);

  if (index === -1) {
    return undefined;
  }

  const value = args[index + 1];
  if (value === undefined || value.startsWith("-")) {
    throw new Error(`Missing value for ${flag}.`);
  }

  return value;
}

function conciseGateOutput(output: string): string[] {
  return output
    .trim()
    .split(/\r?\n/)
    .map((line) => line.replaceAll(`${process.cwd()}${path.sep}`, ""))
    .filter((line) => line.trim() !== "")
    .slice(0, 24);
}

function displayCommandArg(arg: string): string {
  const relativePath = path.relative(process.cwd(), arg);

  if (
    relativePath !== "" &&
    !relativePath.startsWith("..") &&
    !path.isAbsolute(relativePath)
  ) {
    return relativePath.split(path.sep).join("/");
  }

  return arg;
}

async function runGateCommand(
  command: MinifyHtmlGateCommand,
  cwd: string,
): Promise<MinifyHtmlGateResult> {
  // Coverage note: this is the process boundary for validation gates.
  // Tests inject a gate runner to avoid launching nested validation commands.
  return new Promise<MinifyHtmlGateResult>((resolve) => {
    const child = spawn("bunx", command.args, {
      cwd,
      env: commandEnvironment(),
      stdio: ["ignore", "pipe", "pipe"],
    });
    const chunks: Buffer[] = [];

    child.stdout.on("data", (chunk: Buffer) => {
      chunks.push(chunk);
    });
    child.stderr.on("data", (chunk: Buffer) => {
      chunks.push(chunk);
    });
    child.on("error", (error) => {
      resolve({
        command,
        exitCode: 1,
        output: error.message,
      });
    });
    child.on("close", (code) => {
      resolve({
        command,
        exitCode: code ?? 1,
        output: Buffer.concat(chunks).toString("utf8"),
      });
    });
  });
}

async function runScenarioGates({
  cwd,
  gateRunner,
  outputDir,
}: {
  cwd: string;
  gateRunner: MinifyHtmlGateRunner;
  outputDir: string;
}): Promise<MinifyHtmlGateResult[]> {
  return [await gateRunner(htmlValidateCommand(outputDir), cwd)];
}

function scenarioTable(results: MinifyHtmlScenarioResult[]): string {
  return [
    "| Scenario | Policy | HTML Raw Delta | HTML Gzip Delta | HTML Brotli Delta | HTML Validate | Production Eligible |",
    "| --- | --- | ---: | ---: | ---: | --- | --- |",
    ...results.map(
      (result) =>
        `| \`${result.scenario.name}\` | ${result.scenario.policy} | ${formatDelta(htmlRawDelta(result))} | ${formatDelta(htmlGzipDelta(result))} | ${formatDelta(htmlBrotliDelta(result))} | ${result.gates.map(formatGate).join(", ")} | ${result.productionEligible ? "yes" : "no"} |`,
    ),
  ].join("\n");
}

function scenariosByName(names: string[]): MinifyHtmlScenario[] {
  return names.map((name) => {
    if (!isMinifyHtmlExperimentName(name)) {
      throw new Error(
        `Unknown minify-html scenario "${name}". Expected one of: ${minifyHtmlExperimentNames.join(", ")}.`,
      );
    }

    const scenario = defaultScenarios.find((item) => item.name === name);
    if (scenario === undefined) {
      throw new Error(`No scenario metadata configured for "${name}".`);
    }

    return scenario;
  });
}

function isMinifyHtmlExperimentName(
  name: string,
): name is MinifyHtmlExperimentName {
  return minifyHtmlExperimentNames.some(
    (experimentName) => experimentName === name,
  );
}

function usage(): string {
  return `Usage: bun run payload:minify-html:experiments [--source <dir>] [--out <dir>] [--report <file>] [--scenario <name>] [--require-production-candidate] [--json]

Run reproducible minify-html scenarios against copied build output, validate
each generated output, measure raw/gzip/Brotli deltas, and write a Markdown
report. Use the package script to rebuild the raw baseline first, or pass a
custom --source directory.

Use --scenario multiple times to limit the suite. Default report path:
${defaultReportFile}`;
}

// Coverage note: this wrapper only connects the exported CLI workflow to
// process exit state; tests call `runMinifyHtmlExperimentSuiteCli()` directly.
if (import.meta.main) {
  try {
    process.exitCode = await runMinifyHtmlExperimentSuiteCli();
  } catch (error) {
    console.error(error instanceof Error ? error.message : String(error));
    process.exitCode = 1;
  }
}
